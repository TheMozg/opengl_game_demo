OpenGLModelType Cube::type() {
    return OpenGLModelType::Cube;
}

bool Cube::collidesWith(glm::vec3 point, float offset) {
    glm::vec2 offsetVec2(offset, offset);
    return collidesWith(point, offsetVec2, offsetVec2, offsetVec2);
}

bool Cube::collidesWith(glm::vec3 point, glm::vec2 xoffset, glm::vec2 yoffset, glm::vec2 zoffset) {
    return (point.x >= mPosition.x - (0.5 + xoffset.x) && point.x <= mPosition.x + (0.5 + xoffset.y)) &&
           (point.y >= mPosition.y - (0.5 + yoffset.x) && point.y <= mPosition.y + (0.5 + yoffset.y)) &&
           (point.z >= mPosition.z - (0.5 + zoffset.x) && point.z <= mPosition.z + (0.5 + zoffset.y));
}
