bool Cube::collidesWith(glm::vec3 point) {
    return (point.x >= mPosition.x - 0.7 && point.x <= mPosition.x + 0.7) &&
        (point.y >= mPosition.y - 0.7 && point.y <= mPosition.y + 0.7) &&
        (point.z >= mPosition.z - 0.7 && point.z <= mPosition.z + 0.7);
}
